"""
Cost-model helpers for the frontier vs. local eval comparison.

Used by ``eval_harness.py`` (local amortized GPU cost) and by spreadsheet /
notebook cost narratives that consume ``eval_results.csv``.
"""

from __future__ import annotations

import logging
from pathlib import Path
from typing import Any

import yaml

logger = logging.getLogger(__name__)

PRICING_CONFIG_PATH = Path(__file__).parent / "pricing.yaml"

DEFAULT_PRICING: dict[str, Any] = {
    "frontier_models": {
        "anthropic": {
            "input_per_million": 3.00,
            "output_per_million": 15.00,
        },
        "openai": {
            "input_per_million": 2.50,
            "output_per_million": 10.00,
        },
    },
    "local_compute": {
        "gpu_hourly_rate_usd": 0.80,
        "reference_docs_per_hour": {
            "classification": 12000,
            "extraction": 2400,
            "memo_generation": 180,
        },
    },
}

_PRICING_YAML_TEMPLATE = """\
# Frontier API + local GPU pricing used by evaluation/eval_harness.py
# and evaluation/cost_model_helpers.py.
#
# Rates are approximate USD list prices for cost-model spreadsheet feeds.
# Update when OpenRouter / provider pricing changes.
#
# Auto-generated by evaluation.cost_model_helpers.ensure_pricing_yaml().

frontier_models:
  anthropic:
    # anthropic/claude-sonnet-4.5 via OpenRouter (approx.)
    input_per_million: 3.00
    output_per_million: 15.00
  openai:
    # openai/gpt-4o via OpenRouter (approx.)
    input_per_million: 2.50
    output_per_million: 10.00

local_compute:
  # Amortized fully-loaded cost of a single consumer/pro GPU hour used for
  # local classifier / extractor / LoRA memo inference. Tune to your hardware.
  gpu_hourly_rate_usd: 0.80
  # Peak / reference throughput (docs per hour) for spreadsheet steady-state $/doc.
  reference_docs_per_hour:
    classification: 12000
    extraction: 2400
    memo_generation: 180
"""


def ensure_pricing_yaml(path: Path | None = None) -> Path:
    """Create ``pricing.yaml`` with committed defaults when the file is missing."""
    path = Path(path) if path is not None else PRICING_CONFIG_PATH
    if path.exists():
        return path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(_PRICING_YAML_TEMPLATE, encoding="utf-8")
    logger.warning("Created missing pricing config at %s", path)
    return path


def _validate_pricing(raw: Any) -> dict[str, Any]:
    if not isinstance(raw, dict):
        raise ValueError("pricing.yaml root must be a mapping")
    frontier = raw.get("frontier_models")
    if not isinstance(frontier, dict) or not frontier:
        raise ValueError("pricing.yaml missing frontier_models")
    for name, entry in frontier.items():
        if not isinstance(entry, dict):
            raise ValueError(f"frontier_models.{name} must be a mapping")
        for key in ("input_per_million", "output_per_million"):
            if key not in entry:
                raise ValueError(f"frontier_models.{name} missing {key}")
            float(entry[key])
    local = raw.get("local_compute")
    if not isinstance(local, dict) or "gpu_hourly_rate_usd" not in local:
        raise ValueError("pricing.yaml missing local_compute.gpu_hourly_rate_usd")
    float(local["gpu_hourly_rate_usd"])
    return raw


def load_pricing_raw(
    path: Path | None = None, *, create_if_missing: bool = True
) -> dict[str, Any]:
    """Load and validate pricing.yaml; optionally create defaults if absent."""
    path = Path(path) if path is not None else PRICING_CONFIG_PATH
    if create_if_missing:
        ensure_pricing_yaml(path)
    try:
        with open(path, encoding="utf-8") as f:
            raw = yaml.safe_load(f)
        return _validate_pricing(raw)
    except Exception as exc:
        logger.warning(
            "Failed to load pricing from %s (%s); using built-in defaults", path, exc
        )
        return dict(DEFAULT_PRICING)


def frontier_cost_usd(
    input_per_million: float,
    output_per_million: float,
    input_tokens: int,
    output_tokens: int,
) -> float:
    return (input_tokens / 1_000_000) * input_per_million + (
        output_tokens / 1_000_000
    ) * output_per_million


def local_cost_per_call(gpu_hourly_rate_usd: float, latency_seconds: float) -> float:
    """Amortized $/call from wall-clock GPU occupancy."""
    if latency_seconds < 0:
        raise ValueError("latency_seconds must be non-negative")
    return gpu_hourly_rate_usd * (latency_seconds / 3600.0)


def local_cost_per_doc_at_throughput(
    gpu_hourly_rate_usd: float, docs_per_hour: float
) -> float:
    """Steady-state $/doc assuming sustained throughput (not per-call latency)."""
    if docs_per_hour <= 0:
        raise ValueError("docs_per_hour must be positive")
    return gpu_hourly_rate_usd / docs_per_hour
